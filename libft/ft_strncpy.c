#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	char *dst_begin;

	dst_begin = dst;
	while (*src && len > 0)
	{
		*dst = *src;
		dst++;
		src++;
		len--;
	}
	while (len > 0)
	{
		*dst = '\0';
		dst++;
		len--;
	}
	return (dst_begin);
}
